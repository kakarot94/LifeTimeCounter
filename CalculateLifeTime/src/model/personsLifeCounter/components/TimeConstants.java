package model.personsLifeCounter.components;

/**
 * This interface was used for easier navigation.
 * 
 * @see {@link #Y} stands for years
 * @see {@link #MM} stands for months
 * @see {@link #DD} stands for days
 * @see {@link #H} stands for hours
 * @see {@link #MIN} stands for minutes
 * @see {@link #SEC} stands for seconds
 * @see {@link #BDAY} stands for birth day
 * @see {@link #BMONTH} stands for birth month
 * @see {@link #BYEAR} stands for birth year
 * @see {@link #DDAY} stands for death day
 * @see {@link #DMONTH} stands for death month
 * @see {@link #DYEAR} stands for death year
 * @author Igor Stojanovic
 *
 */
public interface TimeConstants {
	/**
	 * String "years"
	 */
	public final String Y = "years";
	/**
	 * String "months"
	 */
	public final String MM = "months";
	/**
	 * String "days"
	 */
	public final String DD = "days";
	/**
	 * String "hours"
	 */
	public final String H = "hours";
	/**
	 * String "minutes"
	 */
	public final String MIN = "minutes";
	/**
	 * String "seconds"
	 */
	public final String SEC = "seconds";

	/**
	 * String "birth year"
	 */
	public final String BYEAR = "birth year";
	/**
	 * String "birth month"
	 */
	public final String BMONTH = "birth month";
	/**
	 * String "birth day"
	 */
	public final String BDAY = "birth day";
	/**
	 * String "death year"
	 */
	public final String DYEAR = "death year";
	/**
	 * String "death month"
	 */
	public final String DMONTH = "death month";
	/**
	 * String "death day"
	 */
	public final String DDAY = "death day";
}
